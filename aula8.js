// ARITIMÉTICOS  (   + , - , / , * e **  )
let salario = 5;

console.log(salario + salario);
console.log(salario - salario);
console.log(salario * salario);
console.log(salario / salario);
console.log(salario ** salario); // exponecial

// ++ --  incremento e decremento
let idade = 33;

console.log(++idade); // Idade + 1  >> 34
console.log(idade); // sempre que eu imprimir, vai somar 1 >> 34
console.log(idade); // sempre que eu imprimir, vai somar 1 >> 34

let idade2 = 10;

console.log(idade2); // 10 >> Idade vai vir normal porque ta na linha anterior  ao operador --
console.log(--idade2); // 9  >> idade anterior - 1
console.log(--idade2); // 8  >> idade anterior - 1
console.log(--idade2); // 7  >> idade anterior - 1
console.log(idade2); // 7  >> idade da linha anterior
// Operadores de Atribuição
// É o que estamos usando desde o início. Ex:

let vtg = 100;

vtg += vtg; // vtg = vtg + vtg;  (pode usar + - * /)
console.log(vtg); // 200

//exemplo 2:
vtg -= vtg; // vtg = vtg - vtg; 
console.log(vtg); // 0

//exemplo 3:
vtg **= vtg; // vtg = vtg ** vtg; 
console.log(vtg); // 11